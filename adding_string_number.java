//If  add two numbers, the result will be a number:
 int x = 10;
int y = 20;
int z = x + y;  // z will be 30 (an integer/number)


//If you  two strings, the result will be a string concatenation:
 String x = "10";
String y = "20";
String z = x + y;  // z will be 1020 (a String)


//If  add a number and a string, the result will be a string concatenation:
String x = "10";
int y = 20;
